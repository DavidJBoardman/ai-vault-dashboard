const AUTO_GROUP_NAME_SENTINEL = "__AUTO_COMPOSED_GROUP_NAME__";
const LEGACY_AUTO_GROUP_NAME_PATTERN = /^Group \d+$/i;
const RIB_NAME_DELIMITER = /\s*-\s*/;

function isAutoGeneratedGroupName(name?: string | null): boolean {
  const trimmed = name?.trim();
  return !trimmed || trimmed === AUTO_GROUP_NAME_SENTINEL || LEGACY_AUTO_GROUP_NAME_PATTERN.test(trimmed);
}

function parseRibPathName(name: string): [string, string] | null {
  const parts = name
    .split(RIB_NAME_DELIMITER)
    .map(part => part.trim())
    .filter(Boolean);

  if (parts.length !== 2 || parts[0] === parts[1]) {
    return null;
  }

  return [parts[0], parts[1]];
}

function composeConnectedRibPathName(paths: Array<{ name: string; order: number; nodes: [string, string] }>): string {
  const adjacency = new Map<string, string[]>();

  paths.forEach(path => {
    const [start, end] = path.nodes;
    adjacency.set(start, [...(adjacency.get(start) ?? []), end]);
    adjacency.set(end, [...(adjacency.get(end) ?? []), start]);
  });

  if (Array.from(adjacency.values()).some(neighbors => neighbors.length > 2)) {
    return paths.map(path => path.name).join(" + ");
  }

  const endpoints = Array.from(adjacency.entries())
    .filter(([, neighbors]) => neighbors.length === 1)
    .map(([node]) => node);

  if (adjacency.size === 1) {
    return Array.from(adjacency.keys())[0];
  }

  if (endpoints.length !== 2) {
    return paths.map(path => path.name).join(" + ");
  }

  const firstPath = paths[0].nodes;
  const start = firstPath.find(node => endpoints.includes(node)) ?? endpoints[0];
  const orderedNodes = [start];
  let previous: string | null = null;
  let current = start;

  while (true) {
    const nextNodes = (adjacency.get(current) ?? []).filter(node => node !== previous);
    if (nextNodes.length === 0) {
      break;
    }
    if (nextNodes.length > 1) {
      return paths.map(path => path.name).join(" + ");
    }
    const next = nextNodes[0];
    orderedNodes.push(next);
    previous = current;
    current = next;
    if (orderedNodes.length > adjacency.size) {
      return paths.map(path => path.name).join(" + ");
    }
  }

  if (orderedNodes.length !== adjacency.size) {
    return paths.map(path => path.name).join(" + ");
  }

  return orderedNodes.join("-");
}

function composeRibGroupName(names: string[]): string | undefined {
  if (names.length === 0) {
    return undefined;
  }

  const parsedPaths = names.map((name, order) => {
    const nodes = parseRibPathName(name);
    return nodes ? { name, order, nodes } : null;
  });

  if (parsedPaths.some(path => path == null)) {
    return names.join(" + ");
  }

  const remaining = [...(parsedPaths as Array<{ name: string; order: number; nodes: [string, string] }>)];
  const components: Array<{ order: number; label: string }> = [];

  while (remaining.length > 0) {
    const current = [remaining.shift()!];
    const visited = new Set(current.flatMap(path => path.nodes));

    let changed = true;
    while (changed) {
      changed = false;
      for (let index = remaining.length - 1; index >= 0; index -= 1) {
        const path = remaining[index];
        if (path.nodes.some(node => visited.has(node))) {
          current.push(path);
          path.nodes.forEach(node => visited.add(node));
          remaining.splice(index, 1);
          changed = true;
        }
      }
    }

    current.sort((left, right) => left.order - right.order);
    components.push({
      order: current[0].order,
      label: composeConnectedRibPathName(current),
    });
  }

  components.sort((left, right) => left.order - right.order);
  return components.map(component => component.label).join(" + ");
}



function median(values: number[]): number {
  if (values.length === 0) return 0;
  const sorted = [...values].sort((a, b) => a - b);
  const mid = Math.floor(sorted.length / 2);
  return sorted.length % 2 !== 0 ? sorted[mid] : (sorted[mid - 1] + sorted[mid]) / 2;
}

/**
 * Ratio of radius-to-rib-length above which the arc is considered a straight line.
 * R > 10 * L means the sagitta is ~1.25% of L — visually imperceptible curvature.
 */
const STRAIGHT_LINE_RATIO = 10;

function formatRadius(r: number | undefined | null, ribLength?: number | null): string {
  if (r == null || r <= 0) return "N/A";
  const threshold = (ribLength != null && ribLength > 0)
    ? STRAIGHT_LINE_RATIO * ribLength
    : 500;
  if (r > threshold) return "N/A";
  return `${r.toFixed(2)}m`;
}

/**
 * Convert normalized error value (0-1) to a color gradient (green to red)
 */
function errorToColor(normalizedError: number): string {
  const t = Math.max(0, Math.min(1, normalizedError));

  let r: number;
  let g: number;

  if (t < 0.5) {
    // Green â†’ Yellow
    const localT = t * 2; // scale 0â€“0.5 to 0â€“1
    r = Math.round(255 * localT);
    g = 255;
  } else {
    // Yellow â†’ Red
    const localT = (t - 0.5) * 2; // scale 0.5â€“1 to 0â€“1
    r = 255;
    g = Math.round(255 * (1 - localT));
  }

  return `rgb(${r}, ${g}, 0)`;
}

/**
 * Uniformly downsample paired point/distance arrays for display only.
 * Full-precision arrays are preserved for measurement calculations.
 */
function decimateSegmentsForDisplay(
  segmentPoints: Point3D[],
  pointDistances: number[],
  maxPoints: number,
): { points: Point3D[]; distances: number[] } {
  if (segmentPoints.length <= maxPoints || maxPoints < 2) {
    return { points: segmentPoints, distances: pointDistances };
  }
  const step = (segmentPoints.length - 1) / (maxPoints - 1);
  const points: Point3D[] = [];
  const distances: number[] = [];
  for (let i = 0; i < maxPoints - 1; i++) {
    const idx = Math.round(i * step);
    points.push(segmentPoints[idx]);
    distances.push(pointDistances[idx]);
  }
  points.push(segmentPoints[segmentPoints.length - 1]);
  distances.push(pointDistances[pointDistances.length - 1]);
  return { points, distances };
}

/**
 * Create colored line segments from trace points and error distances
 */
function createColoredTraceLines(
  segmentPoints: Point3D[],
  pointDistances: number[],
  traceId: string,
  isSelected: boolean = false
): Line3D[] {
  if (segmentPoints.length < 2 || pointDistances.length === 0) {
    return [];
  }
  
  // Find min and max distances for normalization
  const minDist = Math.min(...pointDistances);
  const maxDist = Math.max(...pointDistances);
  const range = maxDist - minDist || 1;
  
  // Create line segments between consecutive points
  const lines: Line3D[] = [];
  
  for (let i = 0; i < segmentPoints.length - 1; i++) {
    // Use average of the two endpoints' errors for the segment color
    const error1 = pointDistances[i];
    const error2 = pointDistances[i + 1];
    const avgError = (error1 + error2) / 2;
    
    // Normalize error to 0-1 range
    const normalizedError = Math.abs((avgError - minDist) / range);
    
    // If selected, use full color gradient; otherwise use neutral gray
    const color = isSelected ? "rgb(180, 180, 180)" : errorToColor(normalizedError);
    
    lines.push({
      id: `${traceId}-segment-${i}`,
      label: `Segment ${i + 1}`,
      color,
      points: [segmentPoints[i], segmentPoints[i + 1]],
    });
  }
  
  return lines;
}

function createBestFitArcLines(
  segmentPoints: Point3D[],
  arcCenter: Point3D,
  arcRadius: number,
  traceId: string,
  fitBasisU?: Point3D,
  fitBasisV?: Point3D,
  fitStartAngle?: number,
  fitEndAngle?: number,
): Line3D[] {
  if (segmentPoints.length < 3 || !arcCenter || arcRadius <= 0) return [];

  const arcColor = "rgb(100, 150, 255)";
  const lines: Line3D[] = [];

  const hasBackendArcFrame =
    !!fitBasisU &&
    !!fitBasisV &&
    Number.isFinite(fitStartAngle) &&
    Number.isFinite(fitEndAngle);

  let u: Point3D;
  let v: Point3D;
  let startAngle: number;
  let endAngle: number;

  if (hasBackendArcFrame) {
    u = fitBasisU;
    v = fitBasisV;
    startAngle = fitStartAngle as number;
    endAngle = fitEndAngle as number;
  } else {

    // --- 1. Compute robust normal using cross of endpoints ---
    const pStart = segmentPoints[0];
    const pMid = segmentPoints[Math.floor(segmentPoints.length / 2)];
    const pEnd = segmentPoints[segmentPoints.length - 1];

    const v1 = {
      x: pMid.x - pStart.x,
      y: pMid.y - pStart.y,
      z: pMid.z - pStart.z,
    };

    const v2 = {
      x: pEnd.x - pStart.x,
      y: pEnd.y - pStart.y,
      z: pEnd.z - pStart.z,
    };

    let normal = {
      x: v1.y * v2.z - v1.z * v2.y,
      y: v1.z * v2.x - v1.x * v2.z,
      z: v1.x * v2.y - v1.y * v2.x,
    };

    const normalLen = Math.hypot(normal.x, normal.y, normal.z);
    if (normalLen === 0) return [];

    normal = {
      x: normal.x / normalLen,
      y: normal.y / normalLen,
      z: normal.z / normalLen,
    };

    // --- 2. Build basis ---
    const firstVec = {
      x: pStart.x - arcCenter.x,
      y: pStart.y - arcCenter.y,
      z: pStart.z - arcCenter.z,
    };

    const uLen = Math.hypot(firstVec.x, firstVec.y, firstVec.z);
    if (uLen === 0) return [];

    u = {
      x: firstVec.x / uLen,
      y: firstVec.y / uLen,
      z: firstVec.z / uLen,
    };

    v = {
      x: normal.y * u.z - normal.z * u.y,
      y: normal.z * u.x - normal.x * u.z,
      z: normal.x * u.y - normal.y * u.x,
    };

    // --- 3. Compute angles ---
    const angles = segmentPoints.map((p) => {
      const vec = {
        x: p.x - arcCenter.x,
        y: p.y - arcCenter.y,
        z: p.z - arcCenter.z,
      };

      const dotU = vec.x * u.x + vec.y * u.y + vec.z * u.z;
      const dotV = vec.x * v.x + vec.y * v.y + vec.z * v.z;

      return Math.atan2(dotV, dotU);
    });

    // --- 4. Unwrap in trace order to preserve the full traced extent ---
    const unwrappedAngles: number[] = [angles[0]];
    for (let i = 1; i < angles.length; i++) {
      let next = angles[i];
      const prev = unwrappedAngles[i - 1];
      while (next - prev > Math.PI) {
        next -= 2 * Math.PI;
      }
      while (next - prev < -Math.PI) {
        next += 2 * Math.PI;
      }
      unwrappedAngles.push(next);
    }

    startAngle = unwrappedAngles[0];
    endAngle = unwrappedAngles[unwrappedAngles.length - 1];
  }

  // --- 5. Return as true mathematical arc with parameters ---
  // Sample just the endpoints for the preview spheres
  const arcPoints: Point3D[] = [
    {
      x: arcCenter.x + arcRadius * (Math.cos(startAngle) * u.x + Math.sin(startAngle) * v.x),
      y: arcCenter.y + arcRadius * (Math.cos(startAngle) * u.y + Math.sin(startAngle) * v.y),
      z: arcCenter.z + arcRadius * (Math.cos(startAngle) * u.z + Math.sin(startAngle) * v.z),
    },
    {
      x: arcCenter.x + arcRadius * (Math.cos(endAngle) * u.x + Math.sin(endAngle) * v.x),
      y: arcCenter.y + arcRadius * (Math.cos(endAngle) * u.y + Math.sin(endAngle) * v.y),
      z: arcCenter.z + arcRadius * (Math.cos(endAngle) * u.z + Math.sin(endAngle) * v.z),
    },
  ];

  lines.push({
    id: `${traceId}-ideal-arc`,
    label: `Ideal Arc`,
    color: arcColor,
    points: arcPoints,
    arc: {
      center: arcCenter,
      radius: arcRadius,
      startAngle,
      endAngle,
      u,
      v,
    },
  });

  return lines;
}

